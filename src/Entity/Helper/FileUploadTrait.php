<?php

namespace App\Entity\Helper;

use Symfony\Component\HttpFoundation\File\{File, UploadedFile};


trait FileUploadTrait
{
    private $file;

    /**
     * Get file.
     *
     * @return UploadedFile
     */
    public function getFile()
    {
        return $this->file;
    }

    /**
     * Sets file.
     *
     * @param UploadedFile $file
     */
    public function setFile(UploadedFile $file = null)
    {
        $this->file = $file;
    }

}
